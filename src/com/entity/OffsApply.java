package com.entity;

public class OffsApply {
	private int id;
	private String curtime;
	private String state;
	private int offsId;
	private Offs data;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCurtime() {
		return curtime;
	}

	public void setCurtime(String curtime) {
		this.curtime = curtime;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public int getOffsId() {
		return offsId;
	}

	public void setOffsId(int offsId) {
		this.offsId = offsId;
	}

	public Offs getData() {
		return data;
	}

	public void setData(Offs data) {
		this.data = data;
	}

}
